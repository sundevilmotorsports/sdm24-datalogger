void INA260_WriteRegister(uint8_t reg, uint16_t data) {
	uint8_t	buffer[3];

	buffer[0] = reg;
	buffer[1] = (uint8_t)(data >> 8);
	buffer[2] = (uint8_t)(data);

	HAL_I2C_Master_Transmit(&hi2c2, INA260_ADDRESS << 1, buffer, sizeof(buffer), HAL_MAX_DELAY);
}

void INA_260_ReadRegister(uint8_t reg, uint16_t *data) {
	HAL_I2C_Master_Transmit(&hi2c2, INA260_ADDRESS << 1, &reg, 1, HAL_MAX_DELAY);
	HAL_I2C_Master_Receive(&hi2c2, (INA260_ADDRESS << 1) | 1, (uint8_t *)data, 2, HAL_MAX_DELAY);
}

float getVoltage(void) {
	uint16_t voltageData;
	INA_260_ReadRegister(0x02, &voltageData);

	float voltage = voltageData * 0.00125; // 1.25 mV per LSB

	return voltage;
}

float getCurrent(void) {
	uint16_t currentData;
	INA_260_ReadRegister(0x01, &currentData);

	float current = currentData * 0.00125; // 1.25 mA per LSB

	return current;
}
